    print('        // (Task #509 bullet 코드 17 종이 KTUG 매핑 표에 없는지 검증)')
    print('        let task_509_bullets = [')
    print('            0xF0A0_u32, 0xF0E8, 0xF02EF,')
    print('            0xF02B1, 0xF02B2, 0xF02B3, 0xF02B4, 0xF02B5,')
    print('            0xF02B6, 0xF02B7, 0xF02B8, 0xF02B9,')
