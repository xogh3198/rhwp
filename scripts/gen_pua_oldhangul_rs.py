def main():
    if len(sys.argv) != 2:
        print("Usage: gen_pua_oldhangul_rs.py <hypua2jamocomposed.txt>", file=sys.stderr)
        sys.exit(1)
    mapping = parse_ktug(sys.argv[1])
    emit_rust(mapping)

if __name__ == '__main__':
    main()
